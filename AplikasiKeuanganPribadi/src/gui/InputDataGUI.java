    private void BtHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel) TbDuit.getModel();
        if (TbDuit.getSelectedRow()==-1) {
            if (TbDuit.getSelectedRow()==0){
                JOptionPane.showMessageDialog(null, "Tidak Ada Data yang Di Pilih");
            }else{
                JOptionPane.showMessageDialog(null,"Silahkan Pilih Data");
            }
        }else{
            int a=JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus Data Tersebut ?", "select", JOptionPane.YES_NO_OPTION);
        if (a==0)
        {
            
             // mengambil nilai uang masuk, keluar, dan selisih
            double masukLama = Double.parseDouble(model.getValueAt(TbDuit.getSelectedRow(), 1).toString().replace("Rp. ", ""));
            double keluarLama = Double.parseDouble(model.getValueAt(TbDuit.getSelectedRow(), 2).toString().replace("Rp. ", ""));
            double selisihLama = Double.parseDouble(model.getValueAt(TbDuit.getSelectedRow(), 3).toString().replace("Rp. ", ""));
            
            // mengurangi keseluruhan nilai dengan nilai yang lama
            totalMasuk -= masukLama;
            totalKeluar -= keluarLama;
            totalSelisih -= selisihLama;
            
             model.removeRow(TbDuit.getSelectedRow());
             JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
              // Tampilkan saldo
            LbTotal.setText("Uang yang anda miliki : Rp. " + String.valueOf(totalSelisih));
        }    
       }
    }//GEN-LAST:event_BtHapusActionPerformed

    private void BtTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtTambahActionPerformed
         if (TfTanggal.getText().trim().equals("") || TfAlasan.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(null, "Tolong Isi Data Yang Kosong  !!! \n Silahkan lengkapi");
    } else {
        String masukText = TfMasuk.getText().replace("Rp. ", ""); // Mengambil nilai tanpa "Rp. "
        String keluarText = TfKeluar.getText().replace("Rp. ", ""); // Mengambil nilai tanpa "Rp. "
        
        // Menghitung selisih antara TfMasuk dan TfKeluar
        double masuk = Double.parseDouble(masukText.isEmpty() ? "0" : masukText);
        double keluar = Double.parseDouble(keluarText.isEmpty() ? "0" : keluarText);
        double selisih = masuk - keluar;
        
        // Menambahkan data ke dalam tabel
        DefaultTableModel model = (DefaultTableModel) TbDuit.getModel();
        model.addRow(new Object[]{TfTanggal.getText(), "Rp. " + masuk, "Rp. " + keluar, "Rp. " + selisih, TfAlasan.getText()});
        JOptionPane.showMessageDialog(null, "Data anda berhasil di input");
        
        // Memperbarui total uang masuk dan keluar
        totalMasuk += masuk;
        totalKeluar += keluar;
        totalSelisih += selisih;
        
        // Tampilkan saldo
            LbTotal.setText("Uang yang anda miliki : Rp. " + String.valueOf(totalSelisih));
    }
        TfTanggal.setText("");
        TfMasuk.setText("Rp. ");
        TfKeluar.setText("Rp. ");
        TfAlasan.setText("");
    }//GEN-LAST:event_BtTambahActionPerformed

    private void BtCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtCetakActionPerformed
         try {
            BufferedWriter bw = new BufferedWriter(new FileWriter("DataUang.txt"));
            
            for(int i = 0; i < TbDuit.getRowCount(); i++) { //membaca rows table
                for(int j = 0; j <TbDuit.getColumnCount(); j++) { //membaca column table
                    bw.write(TbDuit.getValueAt(i, j).toString()+" ");
                }
                bw.newLine();
            }
            
           JOptionPane.showMessageDialog(null, "Data Berhasil di Cetak");
           bw.close();
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_BtCetakActionPerformed

    private void BtImporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtImporActionPerformed
        try {
            BufferedReader br = new BufferedReader(new FileReader("ImportData.txt"));
            
            DefaultTableModel model = (DefaultTableModel) TbDuit.getModel();
            Object[] lines = br.lines().toArray();
            
            for (int i = 0; i < lines.length; i++) {
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
                JOptionPane.showMessageDialog(null, "Data Berhasil Import");
            }
           
        } catch (Exception e) {
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_BtImporActionPerformed

