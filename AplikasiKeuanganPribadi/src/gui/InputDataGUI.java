    private void BtSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtSimpanActionPerformed
        DefaultTableModel model = (DefaultTableModel) TbDuit.getModel();
        if (TbDuit.getSelectedRow()==-1) {
            if (TbDuit.getRowCount()==0) {
                JOptionPane.showMessageDialog(null, "Tidak Ada Data yang Di Pilih");
            }else{
                JOptionPane.showMessageDialog(null, "Silahkan Pilih Data");
            }
        }else{
            
            // mengambil nilai uang masuk, keluar, dan selisih
            double masukLama = Double.parseDouble(model.getValueAt(TbDuit.getSelectedRow(), 1).toString().replace("Rp. ", ""));
            double keluarLama = Double.parseDouble(model.getValueAt(TbDuit.getSelectedRow(), 2).toString().replace("Rp. ", ""));
            double selisihLama = Double.parseDouble(model.getValueAt(TbDuit.getSelectedRow(), 3).toString().replace("Rp. ", ""));
            
            // mengurangi keseluruhan nilai dengan nilai yang lama
            totalMasuk -= masukLama;
            totalKeluar -= keluarLama;
            totalSelisih -= selisihLama;
            
            String masukText = TfMasuk.getText().replace("Rp. ", ""); // Mengambil nilai tanpa "Rp. "
            String keluarText = TfKeluar.getText().replace("Rp. ", ""); // Mengambil nilai tanpa "Rp. "

            // Menghitung selisih antara TfMasuk dan TfKeluar
            double masuk = Double.parseDouble(masukText.isEmpty() ? "0" : masukText);
            double keluar = Double.parseDouble(keluarText.isEmpty() ? "0" : keluarText);
            double selisih = masuk - keluar;
            
            model.setValueAt(TfTanggal.getText(), TbDuit.getSelectedRow(), 0);
            model.setValueAt(TfMasuk.getText(), TbDuit.getSelectedRow(), 1);
            model.setValueAt(TfKeluar.getText(), TbDuit.getSelectedRow(), 2);
            model.setValueAt("Rp. " + String.valueOf(selisih), TbDuit.getSelectedRow(), 3);
            model.setValueAt(TfAlasan.getText(), TbDuit.getSelectedRow(), 4);
            
            // menambahkan keseluruhan nilai dengan yang baru
            totalMasuk += masuk;
            totalKeluar += keluar;
            totalSelisih += selisih;
            
            // Tampilkan saldo
            LbTotal.setText("Uang yang anda miliki : Rp. " + String.valueOf(totalSelisih));
            
            BtTambah.setEnabled(true);
            JOptionPane.showMessageDialog((null), "Data berhasil di ubah");
            TfTanggal.setText("");
            TfMasuk.setText("Rp. ");
            TfKeluar.setText("Rp. ");
            TfAlasan.setText(""); 
            TfTanggal.requestFocus();
           
        }
    }//GEN-LAST:event_BtSimpanActionPerformed

